package Algo;

class Edges {
    int src, dest, edgeweight, srcnodeweight, desnodeweight;
    
    Edges(int src, int dest, int edgeweight, int srcnodeweight, int desnodeweight) 
    {
        this.src = src;
        this.dest = dest;
        this.edgeweight = edgeweight;
        this.srcnodeweight = srcnodeweight;
        this.desnodeweight = desnodeweight;
    }
}
